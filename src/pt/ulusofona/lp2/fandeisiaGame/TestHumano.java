        acriaturaTestada.setImagemCriatura("Oeste");
        acriaturaTestada.x = 6;
        acriaturaTestada.y = 4;
        xEsperado = 4;
        acriaturaTestada.moveCriatura();
        assertEquals(xEsperado, acriaturaTestada.getX());
        acriaturaTestada.x = 6;
        acriaturaTestada.y = 5;
        xEsperado = 6;
        acriaturaTestada.moveCriatura();
        assertEquals(xEsperado, acriaturaTestada.getX());
